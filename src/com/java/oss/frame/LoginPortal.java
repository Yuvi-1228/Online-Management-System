    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JOptionPane.showMessageDialog(this,"Successfully Login!!!");
        boolean isSignInSuccess = false;
        try {
            // TODO add your handling code here:

            BufferedReader bw = new BufferedReader(new FileReader(Utils.U_FILENAME));
            String line = bw.readLine();
            while(line != null) {
                String[] userInfoArr = line.split(",");
                if(!userInfoArr[User.USERNAME].isEmpty() && userInfoArr[User.USERNAME].equals(username.getText())
                    && !userInfoArr[User.PASSWORD].isEmpty() && userInfoArr[User.PASSWORD].equals(password.getText())){
                    isSignInSuccess = true;
                    break;
                }
                line = bw.readLine();
            }

            if(isSignInSuccess) {
                String[] userInfoArr = line.split(",");
                user = new User();
                user.setId(Integer.parseInt(userInfoArr[User.ID]));
                user.setFullName(userInfoArr[User.FULL_NAME]);
                user.setAddress(userInfoArr[User.ADDRESS]);
                user.setEmail(userInfoArr[User.EMAIL]);
                user.setPhone(userInfoArr[User.PHONE]);
                user.setUsername(userInfoArr[User.USERNAME]);
                user.setPassword(userInfoArr[User.PASSWORD]);
                try {
                    user.setRole(Integer.parseInt(userInfoArr[User.ROLE]));
                }catch(NumberFormatException e) {
                    user.setRole(User.ROLE_CUSTOMER);
                }
                errorStatusLabelContainer.setVisible(false);

                Dashboard form = new Dashboard();
                form.setVisible(true);

                this.dispose();

            }else {
                user = null;
                System.out.println("Failure");
                errorStatusLabelContainer.setVisible(true);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

